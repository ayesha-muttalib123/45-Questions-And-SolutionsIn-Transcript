// The trim() method is used to remove whitespace characters from the beginning and end of the string.
// and use \n for newline and \t for tab
let name = "\t \n Ayesha \n\t";


console.log("Name with whitespace:");
console.log("(" + name + ")");


let strippedName = name.trim();
console.log("\nName after stripping whitespace:");
console.log("(" + strippedName + ")");
